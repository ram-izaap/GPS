          <a href="#">Why was the HMGPS system created?</a>
        </h4>
      </div>
      <div id="collapse11" class="panel-collapse collapse">
        <div class="panel-body">
<p>The HMGPS System was created by an officer in the US Air Force Civil Air Patrol in response to a real life urgent situation and young child calling for help on a cell phone.  When the child was not able to provide a location and it became apparent quickly that it is very difficult to share your location if you do not know the address or if you are in motion.</p>
<p>What were the most important requirements for development or HMGPS?
1.      The main requirement for the system was that a single police officer while sitting in their squad car, could take a call from a cell phone and within 30 seconds be able to locate the caller on a live map even if the caller has no idea where they are.
2.      The Second requirement was that the officer could join the live map and both he and the person calling for help would be able to see each other as they moved toward each other.
3.      The third requirement was that the officer could coordinate with other police officers, dispatchers, fire and rescue squads or even families of the victim over a two-way radio and phone and provide the map channel verbally so that they could view the channel on a smart device
4.      The system would not be limited to only cell tower triangulation or GPS satellites.  It must use all location signals available in the way Google Location Services functions.
5.      Finally, the system must be inherently simple to use so that an officer could just send the victim a link or direct them to a website to join a live map.
</p>
